package practice;

public class floatandint {
	/*标识符命名规则 数字 字母  _ $  
	   不能 数字开头 
	   不能是JAVA中的关键字
	   区分大小写*/
	public static void main(String[] args) {
		int b1 = 3;
		float b2= 3.9f;
		int b3= (int)(b1+b2);//float取值范围大于INT
		double b4=3.5+6.6;
		System.out.println(b4);
		System.out.println(b3);
		
		

	}

}
